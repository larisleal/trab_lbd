													<h4 style="text-indent: 20px;">OVOS CAIPIRAS</h4>
													<p align=justify style="text-indent: 20px;">
													Os ovos de granja, que são os mais comuns, são gerados por galinhas
													condicionadas à produtividade. Elas ficam confinadas por muito tempo e
													vivem em condições adversas. Os ovos caipiras não. As galinhas
													(conhecidas como poedeiras ou caipiras) recebem uma alimentação mais
													variada de origem vegetal. Elas também não recebem antibióticos ou
													outros hormônios para seu crescimento.

													Esses animais podem viver ao ar livre (em alguns casos apenas durante o
													dia, conhecido como regime semi extensivo) – o que permite que elas
													manifestem seu comportamento natural e sejam menos estressadas.
													</p>

